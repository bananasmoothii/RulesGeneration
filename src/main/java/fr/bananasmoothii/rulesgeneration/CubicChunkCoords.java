package fr.bananasmoothii.rulesgeneration;

public class CubicChunkCoords {

    public CubicChunk cubicChunk;
    public int x, y, z;

    public CubicChunkCoords(CubicChunk cubicChunk, int x, int y, int z) {
        this.cubicChunk = cubicChunk;
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
